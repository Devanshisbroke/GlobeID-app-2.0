/// Country-keyed travel intel: ISO alpha-2 code, currency, primary
/// visa policy + customs cap, time zone offset against UTC, calling
/// code. Used by the Trip detail screen to derive trip-aware values
/// for the Visa / Currency / Time cards instead of hard-coding a
/// single destination (the legacy version was always Schengen/JP).
///
/// Stays inside the app — no network call. Curated to cover every
/// airport country we ship in `domain/airports.dart`.
@immutable
class CountryProfile {
  const CountryProfile({
    required this.country,
    required this.iso2,
    required this.currency,
    required this.currencySymbol,
    required this.utcOffsetHours,
    required this.callingCode,
    required this.visaPolicy,
    required this.stayAllowed,
    required this.passportValidity,
    required this.onwardTicket,
    required this.customsCap,
  });

  /// Country name (matches the `country` field on `Airport`).
  final String country;
  final String iso2;
  final String currency;
  final String currencySymbol;
  final double utcOffsetHours;
  final String callingCode;

  /// Short label like 'VISA-FREE' / 'eVISA' / 'VISA REQ' rendered as a chip.
  final String visaPolicy;
  final String stayAllowed;
  final String passportValidity;
  final String onwardTicket;
  final String customsCap;
}

