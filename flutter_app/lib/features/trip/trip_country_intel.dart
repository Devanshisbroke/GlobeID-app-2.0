  /// Resolve a country profile for a given destination IATA. Falls
  /// back to a neutral profile so callers never null out the card.
  static CountryProfile fromIata(String? iata) {
    final ap = getAirport(iata);
    if (ap == null) return _fallback;
    return _byCountry[ap.country] ?? _fallback;
  }
